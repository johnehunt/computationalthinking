# This file illustrates examples of using for loops with data containerts / collections

# Loop through a list of values
my_list = [1, 2, 3, 4]
for item in my_list:
    print(item)

print('-' * 10)

# Loop through a set of values
my_set = {'a', 'b', 'c'}
for item in my_set:
    print(item)

print('-' * 10)

# Loop through a fixed / immutable tuple of values
my_tuple = (1, 2, 3, 4)
for item in my_tuple:
    print(item)
