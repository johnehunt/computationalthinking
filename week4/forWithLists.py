# This file illustrates examples of using for loops with data containerts / collections

my_list = [1, 2, 3, 4]
for item in my_list:
    print(item)

print('-' * 10)

my_set = {'a', 'b', 'c'}
for item in my_set:
    print(item)

print('-' * 10)

my_tuple = (1, 2, 3, 4)
for item in my_tuple:
    print(item)
